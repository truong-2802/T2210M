<h1>Choáng với số tiền thưởng đội vô địch World Cup 2022, cầu thủ cũng 'nặng túi'</h1>
<p>
    FIFA xác nhận vào tháng 4/2022 rằng nhà vô địch World Cup Qatar sẽ nhận được số tiền thưởng kỷ lục 42 triệu USD, tức là hơn 1.000 tỷ đồng. Kể từ World Cup 2002 trở về trước, các đội vô địch World Cup nhận số tiền thưởng rất khiêm tốn. Nhưng từ năm 2006, số tiền thưởng đã tăng cao đột biến để phù hợp với doanh thu của FIFA.

    Năm 1982, đội tuyển Italia vô địch World Cup chỉ 'bỏ túi' 2,2 triệu USD. Tức là sau 40 năm, tiền thưởng cho chức vô địch World Cup đã tăng lên 20 lần. Năm 1998, đội tuyển Pháp chỉ nhận 6 triệu USD tiền thưởng nhưng con số này đã tăng lên 38 triệu USD khi họ lại lên ngôi vô địch sau 20 năm.

    Ngoài ra, các đội tuyển tham dự World Cup đều nhận được tiền thưởng dựa trên vị trí cuối cùng của mình ở giải đấu. Ví dụ, đội dừng bước ở tứ kết sẽ được thưởng 17 triệu USD. Nếu vào đến bán kết, số tiền thưởng tăng lên đến 25 triệu USD.
</p>
